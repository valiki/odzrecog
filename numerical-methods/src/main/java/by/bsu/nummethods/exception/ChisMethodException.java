package by.bsu.nummethods.exception;

public class ChisMethodException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 169012543464310233L;

	public ChisMethodException() {
		super();
	}

	public ChisMethodException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ChisMethodException(String arg0) {
		super(arg0);
	}

	public ChisMethodException(Throwable arg0) {
		super(arg0);
	}

}
