package by.bsu.nummethods.exception;

public class NotInitializedDataException extends ChisMethodException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8978843056341201014L;

	public NotInitializedDataException() {
		super();
	}

	public NotInitializedDataException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NotInitializedDataException(String arg0) {
		super(arg0);
	}

	public NotInitializedDataException(Throwable arg0) {
		super(arg0);
	}

}
